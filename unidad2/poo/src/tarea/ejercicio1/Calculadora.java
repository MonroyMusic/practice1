package tarea.ejercicio1;

public class Calculadora{
    
    int result;
    
    public int suma(int num1, int num2){
        
        result = num1 + num2; 
        
        return result;

    }
    
    public int resta(int num1, int num2){
        
        result = num1 -  num2; 
        
        return result;

    }
    
    public int multiplicacion(int num1, int num2){
        
        result = num1 * num2; 
        
        return result;

    }
    
    public double division(int num1, int num2){
        
        result = num1 / num2; 
        
        
        return result;

    }
    
}
